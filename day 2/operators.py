# Arithmetic Operators
a = 5
b= 2

## add = a+b (WE can story the sub value in another variable or directly can print)

print(a+b)  # addition of a,b
print(a-b)  # substraction of a,b
print(a*b)  # multiplication of a,b
print(a/b)  # divisal op-2.5
print(a//b) # quotient 2
print(a%b)  # modular division : 2 remainder
print(a**b) # 5**2 square of 5 op-25

# Relational Operator/comparison Operator
#(>,<,>=,<=,!=,==)  it always returns value true/false

a=5
b=10
print(a>b)  # false
print(a<b)  # True
print(a==b)  # false
print(a<=b)  # true
print(a>=b) # false

#Logical Operator
#(and, or, not) it always returns value true/false

