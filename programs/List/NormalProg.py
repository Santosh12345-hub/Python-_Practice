list=[10,20,30]
print(list)   #[10, 20, 30]

#Access the elements using index

print(list[2])  #30
#Negative index (-3,-2,-1)
print(list[-3])

#Slicing list in python

my_list=['a','g','d','f','p','m']
print(my_list[2:4]) #['d', 'f']
print(my_list[2:-1])

#adding new items into list  - add item on last
my_list.append('so')
print(my_list)

